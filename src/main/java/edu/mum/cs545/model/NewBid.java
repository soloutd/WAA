package edu.mum.cs545.model;

public class NewBid {

	private Long carid;
	private String newbid;
	private String highbid;
	
	public Long getCarid() {
		return carid;
	}
	public void setCarid(Long carid) {
		this.carid = carid;
	}
	public String getNewbid() {
		return newbid;
	}
	public void setNewbid(String newbid) {
		this.newbid = newbid;
	}
	public String getHighbid() {
		return highbid;
	}
	public void setHighbid(String highbid) {
		this.highbid = highbid;
	}
	
	
}
